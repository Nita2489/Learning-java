package Session5;

public class ForLoop {
	public static void main(String[] args)
	{
		//while loop
				int i = 1;   //start point
				System.out.println("following are values of m : ");
				while(i<20)
				{
					System.out.println(i);
				    i++;
				}
				
				System.out.println("-------------------");
				// for loop
				for(int b=0; b<=10; b++)
				{
					System.out.println(b);
				}
				
				System.out.println("-------------------");
				
				for(int a=10; a>=0; a--)
				{
					if(a == 3)
					{
						System.out.println(" i got no. three");
					}
					System.out.println(a);
				}
				
				
	}

}
